package week5.lesson3;

public class StringExample04 {
    public static void main(String[] args) {
        String text = "Hello Java";
                    // 0123456789
        System.out.println(text.charAt(0)); // text[0]
        // this method returns you char/symbol at position 0 - 'H'
        System.out.println(text.charAt(4)); // text[4] - 'o'
    }
}
